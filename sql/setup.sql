DROP TABLE IF EXISTS chickens;
DROP TABLE IF EXISTS cats;

CREATE TABLE chickens (
  id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  age INT CHECK (age > 0),
  weight TEXT
);

CREATE TABLE cats (
  id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  lucky BOOLEAN,
  color TEXT NOT NULL,
  lives INT CHECK (lives > 0)
);